/* 
Types of Loops:
Loops allow repetitive tasks to be performed efficiently.

1. for Loop:
The for loop is ideal when the number of iterations is known.
for (let i = 0; i < 5; i++) {
  console.log("Iteration: " + i);
}

2. while Loop:
The while loop runs as long as a specified condition is true.

let i = 0;
while (i < 5) {
  console.log("Iteration: " + i);
  i++;
}


 */